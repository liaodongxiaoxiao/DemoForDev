        if (map.containsKey(key)) {
            return map.get(key)
        }
        return null
    }

    private fun Float.toScaleStr(scale: Int = 2): String {
        val b = BigDecimal(this.toDouble())
        return b.setScale(scale, BigDecimal.ROUND_HALF_UP).toString()
    }
